#с find()
mixed = " Я изучаю PYTHON 3.12 "
cleaned = mixed.strip().lower()
# Находим начало слова "python"
start_index = cleaned.find("python")
# Зная, что "python" имеет длину 6 символов
word = cleaned[start_index:start_index + 6]
# Находим версию в исходной строке (без изменения регистра)
ver_start = mixed.find("3.12")
ver = mixed[ver_start:ver_start + 4]  # "3.12" имеет длину 4
print(f"Программа: {word}, Версия: {ver}")