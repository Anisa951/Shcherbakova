#Пример вывода:
#Значение 100 является числом (int/float)
#Значение 200 НЕ является числом (int/float)
#Значение 300.0 является числом (int/float)

value1 = 100
value2 = "200"
value3 = 300.0
if type(value1)==int or type(value1)==float:
    print(f"Значение {value1} является числом (int/float)")
else:
    print(f"Значение {value1} НЕ является числом (int/float)")
if type(value2) == int or type(value2) == float:
    print(f"Значение {value2} является числом (int/float)")
else:
    print(f"Значение {value2} НЕ является числом (int/float)")
if type(value3)==int or type(value3)==float:
    print(f"Значение {value3} является числом (int/float)")
else:
    print(f"Значение {value3} НЕ является числом (int/float)")

