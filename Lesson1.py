#Проверяет, принадлежит ли тип каждого значения к одному из следующих: целое число (int) или число с плавающей точкой (float).
#В зависимости от результата проверки выводит для каждого значения отдельную строку в формате:
#"Значение <значение> является числом (int/float)"
