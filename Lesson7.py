# Программа должна вывести результаты в формате:
# text
# Всего оценок: X
# Средний балл: X
# Выше среднего: X
# Ниже среднего: X
# Подсказки:
# Все оценки — целые числа
# Для подсчёта выше/ниже среднего понадобится два прохода по данным или два цикла: сначала найти среднее, потом сравнить каждую оценку
# Можно сохранить все оценки в список и работать с ним

with open("grades.txt", "w") as file:
    file.write("85\n")
    file.write("92\n")
    file.write("78\n")
    file.write("90\n")
    file.write("88\n")
    file.write("76\n")
    file.write("95\n")
    file.write("89\n")
with open("grades.txt", "r") as file:
    lines = file.readlines()
grades = []
for line in lines:
    if line.strip() != "":
        grades.append(int(line.strip()))
total = len(grades)
average = round(sum(grades) / total)

above = 0
below = 0
for grade in grades:
    if grade > average:
        above += 1
    elif grade < average:
        below += 1
print(f"Всего оценок: {total}")
print(f"Средний балл: {average}")
print(f"Выше среднего: {above}")
print(f"Ниже среднего: {below}")
