# Задание: Напишите код всех трёх функций. Вызывать их пока не нужно. Пришлите мне код функций.

def validate_username(username):
    if username.isalpha() == True and len(username) >= 3:
        return True
    else:
        return False
def create_welcome_email(username, domain="example.com"):
    return f"{username}@{domain}"

def register_user(username, domain="example.com"):
    if validate_username(username)==False:
        print("Ошибка: недопустимое имя пользователя.")
        return None
    else:
        email = create_welcome_email(username, domain)
        print(f"Пользователь {username} успешно зарегистрирован. Email: {email}.")
        return email


