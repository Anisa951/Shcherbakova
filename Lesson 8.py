# Напишите декоратор с именем repeat_call. Этот декоратор должен вызывать декорируемую функцию ровно два раза подряд.
# Примените этот декоратор к функции say_bye(), которая печатает строку "До свидания!".
# В результате вызов say_bye() должен приводить к такому выводу:
