# Задача 3 (Оператор else)
# Напишите программу, которая запрашивает у пользователя целое число и проверяет, является ли оно чётным.
# Если число чётное, вывести: "Число чётное."
# Иначе (если число нечётное), вывести: "Число нечётное."
# Ваш код должен:
# Получить ввод от пользователя и преобразовать в целое число.
# Использовать оператор if для проверки условия на чётность.
# Использовать оператор else для обработки противоположного случая.
# Напишите свой вариант решения.

number = int(input("Введите число:"))
if number % 2 == 0:
    print("Число чётное.")
else:
    print("Число нечётное.")

# Задача 4 (Оператор elif)
# Напишите программу, которая запрашивает у пользователя оценку (целое число от 1 до 5).
# Программа должна вывести описание оценки:
# Если оценка 5: "Отлично!"
# Если оценка 4: "Хорошо."
# Если оценка 3: "Удовлетворительно."
# Если оценка 2: "Неудовлетворительно."
# Если оценка 1: "Плохо."
# Если введено число не из диапазона 1-5: "Неверная оценка."
# Ваш код должен:
# Получить ввод от пользователя и преобразовать в целое число.
# Использовать цепочку условий if, elif и else для выбора нужной строки.

result = int(input("Введите оценку:"))
if result == 5:
    print("Отлично!")
elif result == 4:
    print("Хорошо.")
elif result == 3:
    print("Удовлетворительно.")
elif result == 2:
    print("Неудовлетворительно.")
elif result == 1:
    print("Плохо.")
else:
    print("Неверная оценка.")

